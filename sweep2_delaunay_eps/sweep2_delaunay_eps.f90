program main

!*****************************************************************************80
!
!! MAIN is the main program for SWEEP2_DELAUNAY_EPS.
!
!  Discussion:
!
!    SWEEP2_DELAUNAY_EPS makes an EPS plot of a SWEEP2 Delaunay triangulation.
!
!  Usage:
!
!    sweep2_delaunay_eps  points.txt  triangles.txt  plot.eps
!
!  Files:
!
!    The input file "points.txt" contains a list of points in the plane.
!    Each line contains the X and Y coordinates of one point.
!
!    The input file "triangles.txt" contains a list of point indices.
!    Each line contains three indices, which form one triangle.
!
!    The output file "plot.eps" is an Encapsulated PostScript file
!    containing an image of the triangulation.
!
!  Licensing:
!
!    This code is distributed under the GNU LGPL license.
!
!  Modified:
!
!    29 January 2005
!
!  Author:
!
!    John Burkardt
!
  implicit none

  character ( len = 100 ) eps_filename
  integer ( kind = 4 ) m
  real ( kind = 8 ), allocatable, dimension ( :, : ) :: point
  character ( len = 100 ) point_filename
  integer ( kind = 4 ) point_num
  integer ( kind = 4 ), allocatable, dimension ( :, : ) :: triangle
  character ( len = 100 ) triangle_filename
  integer ( kind = 4 ) triangle_num

  call timestamp ( )

  write ( *, '(a)' ) ' '
  write ( *, '(a)' ) 'SWEEP2_DELAUNAY_EPS:'
  write ( *, '(a)' ) '  FORTRAN90 version'
  write ( *, '(a)' ) '  Read'
  write ( *, '(a)' ) '    "points.txt", the list of points;'
  write ( *, '(a)' ) '    "triangles.txt", the triangulation created by SWEEP2;'
  write ( *, '(a)' ) '  Create:'
  write ( *, '(a)' ) '    "plot.eps", an Encapsulated PostScript file'
  write ( *, '(a)' ) '    containing an image of the Delaunay triangulation.'

  call get_filenames ( point_filename, triangle_filename, eps_filename )

  write ( *, '(a)' ) ' '
  write ( *, '(a)' ) '  User specification:'
  write ( *, '(a)' ) ' '
  write ( *, '(a)' ) '    point file:    ' // trim ( point_filename )
  write ( *, '(a)' ) '    Triangle file: ' // trim ( triangle_filename )
  write ( *, '(a)' ) '    EPS file:      ' // trim ( eps_filename )
!
!  Get the point information.
!
  call dtable_header_read ( point_filename, m, point_num )

  if ( m /= 2 ) then
    write ( *, '(a)' ) ' '
    write ( *, '(a)' ) 'SWEEP2_DELAUNAY_EPS - Fatal error!'
    write ( *, '(a)' ) '  The point file does not contain a spatial dimension'
    write ( *, '(a)' ) '  of 2!'
    write ( *, '(a)' ) '  Abnormal end of execution.'
    stop
  end if

  if ( point_num <= 0 ) then
    write ( *, '(a)' ) ' '
    write ( *, '(a)' ) 'SWEEP2_DELAUNAY_EPS - Fatal error!'
    write ( *, '(a)' ) '  The point file does not contain a positive number'
    write ( *, '(a)' ) '  of point coordinates.'
    write ( *, '(a)' ) '  Abnormal end of execution.'
    stop
  end if

  allocate ( point(1:2,1:point_num) )

  call dtable_data_read ( point_filename, 2, point_num, point )

  call dtable_print ( 2, point_num, point, '  POINTS:' )
!
!  Get the triangle information.
!
  call itable_header_read ( triangle_filename, m, triangle_num )

  if ( m /= 3 ) then
    write ( *, '(a)' ) ' '
    write ( *, '(a)' ) 'SWEEP2_DELAUNAY_EPS - Fatal error!'
    write ( *, '(a)' ) '  The triangle file does not contain a "spatial"'
    write ( *, '(a)' ) '  dimension of 3!'
    write ( *, '(a)' ) '  Abnormal end of execution.'
    stop
  end if

  if ( triangle_num <= 0 ) then
    write ( *, '(a)' ) ' '
    write ( *, '(a)' ) 'SWEEP2_DELAUNAY_EPS - Fatal error!'
    write ( *, '(a)' ) '  The triangle file does not contain a positive number'
    write ( *, '(a)' ) '  of point coordinates.'
    write ( *, '(a)' ) '  Abnormal end of execution.'
    stop
  end if

  allocate ( triangle(1:3,1:triangle_num) )

  call itable_data_read ( triangle_filename, 3, triangle_num, triangle )
!
!  Adjust the data by adding 1.
!
  triangle(1:3,1:triangle_num) = triangle(1:3,1:triangle_num) + 1

  call itable_print ( 3, triangle_num, triangle, '  TRIANGLES:' )
!
!  Plot the triangles.
!
  call triangulation_plot_eps ( eps_filename, point_num, point, &
    triangle_num, triangle )
!
!  Free memory.
!
  deallocate ( triangle )
  deallocate ( point )
!
!  Terminate.
!
  write ( *, '(a)' ) ' '
  write ( *, '(a)' ) 'SWEEP2_DELAUNAY_EPS:'
  write ( *, '(a)' ) '  Normal end of execution.'

  write ( *, '(a)' ) ' '
  call timestamp ( )

  stop
end
subroutine ch_cap ( c )

!*****************************************************************************80
!
!! CH_CAP capitalizes a single character.
!
!  Licensing:
!
!    This code is distributed under the GNU LGPL license.
!
!  Modified:
!
!    19 July 1998
!
!  Author:
!
!    John Burkardt
!
!  Parameters:
!
!    Input/output, character C, the character to capitalize.
!
  implicit none

  character c
  integer ( kind = 4 ) itemp

  itemp = ichar ( c )

  if ( 97 <= itemp .and. itemp <= 122 ) then
    c = char ( itemp - 32 )
  end if

  return
end
function ch_eqi ( c1, c2 )

!*****************************************************************************80
!
!! CH_EQI is a case insensitive comparison of two characters for equality.
!
!  Example:
!
!    CH_EQI ( 'A', 'a' ) is .TRUE.
!
!  Licensing:
!
!    This code is distributed under the GNU LGPL license.
!
!  Modified:
!
!    28 July 2000
!
!  Author:
!
!    John Burkardt
!
!  Parameters:
!
!    Input, character C1, C2, the characters to compare.
!
!    Output, logical CH_EQI, the result of the comparison.
!
  implicit none

  logical ch_eqi
  character c1
  character c1_cap
  character c2
  character c2_cap

  c1_cap = c1
  c2_cap = c2

  call ch_cap ( c1_cap )
  call ch_cap ( c2_cap )

  if ( c1_cap == c2_cap ) then
    ch_eqi = .true.
  else
    ch_eqi = .false.
  end if

  return
end
subroutine ch_to_digit ( c, digit )

!*****************************************************************************80
!
!! CH_TO_DIGIT returns the integer value of a base 10 digit.
!
!  Example:
!
!     C   DIGIT
!    ---  -----
!    '0'    0
!    '1'    1
!    ...  ...
!    '9'    9
!    ' '    0
!    'X'   -1
!
!  Licensing:
!
!    This code is distributed under the GNU LGPL license.
!
!  Modified:
!
!    04 August 1999
!
!  Author:
!
!    John Burkardt
!
!  Parameters:
!
!    Input, character C, the decimal digit, '0' through '9' or blank
!    are legal.
!
!    Output, integer ( kind = 4 ) DIGIT, the corresponding integer value.  
!    If C was 'illegal', then DIGIT is -1.
!
  implicit none

  character c
  integer ( kind = 4 ) digit

  if ( lge ( c, '0' ) .and. lle ( c, '9' ) ) then

    digit = ichar ( c ) - 48

  else if ( c == ' ' ) then

    digit = 0

  else

    digit = -1

  end if

  return
end
subroutine dtable_data_read ( input_filename, m, n, table )

!*****************************************************************************80
!
!! DTABLE_DATA_READ reads data from a double precision table file.
!
!  Discussion:
!
!    The file may contain more than N points, but this routine will
!    return after reading N of them.
!
!  Licensing:
!
!    This code is distributed under the GNU LGPL license.
!
!  Modified:
!
!    07 September 2004
!
!  Author:
!
!    John Burkardt
!
!  Parameters:
!
!    Input, character ( len = * ) INPUT_FILENAME, the name of the input file.
!
!    Input, integer ( kind = 4 ) M, the spatial dimension.
!
!    Input, integer ( kind = 4 ) N, the number of points.
!
!    Output, real ( kind = 8 ) TABLE(M,N), the table data.
!
  implicit none

  integer ( kind = 4 ) m
  integer ( kind = 4 ) n

  integer ( kind = 4 ) i
  integer ( kind = 4 ) ierror
  character ( len = * ) input_filename
  integer ( kind = 4 ) input_unit
  integer ( kind = 4 ) ios
  character ( len = 255 ) line
  real ( kind = 8 ) table(m,n)
  real ( kind = 8 ) x(m)

  call get_unit ( input_unit )

  open ( unit = input_unit, file = input_filename, status = 'old', &
    iostat = ios )

  if ( ios /= 0 ) then
    ierror = 1
    write ( *, '(a)' ) ' '
    write ( *, '(a)' ) 'DTABLE_DATA_READ - Fatal error!'
    write ( *, '(a)' ) '  Could not open the input file: ' // &
      trim ( input_filename )
    stop
  end if

  i = 0

  do while ( i < n )

    read ( input_unit, '(a)', iostat = ios ) line

    if ( ios /= 0 ) then
      ierror = i
      exit
    end if

    if ( line(1:1) == '#' .or. len_trim ( line ) == 0 ) then
      cycle
    end if

    call s_to_r8vec ( line, m, x, ierror )

    if ( ierror /= 0 ) then
      cycle
    end if

    i = i + 1

    table(1:m,i) = x(1:m)

  end do

  close ( unit = input_unit )

  return
end
subroutine dtable_header_read ( input_filename, m, n )

!*****************************************************************************80
!
!! DTABLE_HEADER_READ reads the header from a double precision table file.
!
!  Licensing:
!
!    This code is distributed under the GNU LGPL license.
!
!  Modified:
!
!    07 September 2004
!
!  Author:
!
!    John Burkardt
!
!  Parameters:
!
!    Input, character ( len = * ) INPUT_FILENAME, the name of the input file.
!
!    Output, integer ( kind = 4 ) M, spatial dimension.
!
!    Output, integer ( kind = 4 ) N, the number of points.
!
  implicit none

  character ( len = * ) input_filename
  integer ( kind = 4 ) m
  integer ( kind = 4 ) n

  call file_column_count ( input_filename, m )

  if ( m <= 0 ) then
    write ( *, '(a)' ) ' '
    write ( *, '(a)' ) 'DTABLE_HEADER_READ - Fatal error!'
    write ( *, '(a)' ) '  There was some kind of I/O problem while trying'
    write ( *, '(a)' ) '  to count the number of data columns in'
    write ( *, '(a)' ) '  the file "' // trim ( input_filename ) // '".'
    stop
  end if

  call file_row_count ( input_filename, n )

  if ( n <= 0 ) then
    write ( *, '(a)' ) ' '
    write ( *, '(a)' ) 'DTABLE_HEADER_READ - Fatal error!'
    write ( *, '(a)' ) '  There was some kind of I/O problem while trying'
    write ( *, '(a)' ) '  to count the number of data rows in'
    write ( *, '(a)' ) '  the file "' // trim ( input_filename ) // '".'
    stop
  end if

  return
end
