program main

!*****************************************************************************80
!
!! MAIN is the main program for TABLE_BORDER.
!
!  Discussion:
!
!    TABLE_BORDER adds the "border" from TABLE data.
!
!    The TABLE data is assumed to be scalar values associated with
!    an M by N spatial grid, but which are stored in a TABLE file
!    as an M * N vector, with one number per record of the file,
!    and with columns preserved.
!
!    Although this is confusing, the data is stored in the file as 
!    though it were a vector, but logically it's really a 2D array,
!    and is treated as such once it is read in.
!
!    (Pie in the Sky): In a future version of this program, 
!    the data at each node will be allowed to be vector-valued.
!    That's one reason we are forcing the data currently to be listed
!    with just a single value per line in the file!
!
!  Licensing:
!
!    This code is distributed under the GNU LGPL license. 
!
!  Modified:
!
!    02 May 2005
!
!  Author:
!
!    John Burkardt
!
  implicit none

  integer ( kind = 4 ) column_num
  integer ( kind = 4 ) column_num2
  logical file_exist
  integer ( kind = 4 ) iarg
  integer ( kind = 4 ) iargc
  integer ( kind = 4 ) ierror
  integer ( kind = 4 ) ihi
  character ( len = 255 ) input_file_name
  character ( len = 255 ) input_file_name_base
  integer ( kind = 4 ) input_file_num
  integer ( kind = 4 ) jhi
  integer ( kind = 4 ) last
  integer ( kind = 4 ) m
  integer ( kind = 4 ) m2
  integer ( kind = 4 ) n
  integer ( kind = 4 ) n2
  integer ( kind = 4 ) numarg
  character ( len = 255 ) output_file_name
  character ( len = 255 ) output_file_name_base
  integer ( kind = 4 ) row_num
  integer ( kind = 4 ) row_num2
  character ( len = 255 ) string
  real ( kind = 8 ), allocatable, dimension ( :, : ) :: u
  real ( kind = 8 ), allocatable, dimension ( :, : ) :: v

  call timestamp ( )

  write ( *, '(a)' ) ' '
  write ( *, '(a)' ) 'TABLE_BORDER:'
  write ( *, '(a)' ) '  FORTRAN90 version'
  write ( *, '(a)' ) ' '
  write ( *, '(a)' ) '* Read a vector representing scalar data on'
  write ( *, '(a)' ) '  an M by N interior grid;'
  write ( *, '(a)' ) ' '
  write ( *, '(a)' ) '* Write a vector representing that scalar data on'
  write ( *, '(a)' ) '  the M+2 by N+2 grid with a boundary;'

  numarg = iargc ( )

  if ( numarg < 1 ) then

    call s_input ( 'Enter the name of the first input file', &
      input_file_name_base, ierror )

    if ( ierror /= 0 ) then
      write ( *, '(a)' ) ' '
      write ( *, '(a)' ) 'TABLE_BORDER - Fatal error!'
      write ( *, '(a)' ) '  Error reading user input.'
      stop
    end if

  else

    iarg = 1
    call getarg ( iarg, input_file_name_base )

  end if

  input_file_name = input_file_name_base
!
!  Get the output filename.
!
  if ( numarg < 2 ) then

    call s_input ( 'Enter the name of the first output file', &
      output_file_name_base, ierror )

    if ( ierror /= 0 ) then
      write ( *, '(a)' ) ' '
      write ( *, '(a)' ) 'TABLE_BORDER - Fatal error!'
      write ( *, '(a)' ) '  Error reading user input.'
      stop
    end if

  else

    iarg = 2
    call getarg ( iarg, output_file_name_base )

  end if

  output_file_name = output_file_name_base
!
!  Get the number of grid rows.
!
  if ( numarg < 3 ) then

    call i4_input ( 'Enter the number of grid rows M', &
      m, ierror )

    if ( ierror /= 0 ) then
      write ( *, '(a)' ) ' '
      write ( *, '(a)' ) 'TABLE_BORDER - Fatal error!'
      write ( *, '(a)' ) '  Error reading user input.'
      stop
    end if

  else

    iarg = 3
    call getarg ( iarg, string )

    call s_to_i4 ( string, m, ierror, last )

    if ( ierror /= 0 ) then
      write ( *, '(a)' ) ' '
      write ( *, '(a)' ) 'TABLE_BORDER - Fatal error!'
      write ( *, '(a)' ) '  Error reading user input.'
      stop
    end if

  end if
!
!  Get the number of grid columns.
!
  if ( numarg < 4 ) then

    call i4_input ( 'Enter the number of grid columns N', &
      n, ierror )

    if ( ierror /= 0 ) then
      write ( *, '(a)' ) ' '
      write ( *, '(a)' ) 'TABLE_BORDER - Fatal error!'
      write ( *, '(a)' ) '  Error reading user input.'
      stop
    end if

  else

    iarg = 4
    call getarg ( iarg, string )

    call s_to_i4 ( string, n, ierror, last )

    if ( ierror /= 0 ) then
      write ( *, '(a)' ) ' '
      write ( *, '(a)' ) 'TABLE_BORDER - Fatal error!'
      write ( *, '(a)' ) '  Error reading user input.'
      stop
    end if

  end if
!
!  Count the number of rows and columns in the input file.
!  For now, we expect COLUMN_NUM = 1.
!
  call dtable_header_read ( input_file_name, column_num, row_num )
!
!  Do we accept the shape of this data?
!
  if ( column_num /= 1 ) then
    write ( *, '(a)' ) ' '
    write ( *, '(a)' ) 'TABLE_BORDER - Fatal error!'
    write ( *, '(a)' ) '  Input file must have just 1 column of values.'
    stop
  end if

  if ( m * n /= row_num ) then
    write ( *, '(a)' ) ' '
    write ( *, '(a)' ) 'TABLE_BORDER - Fatal error!'
    write ( *, '(a)' ) '  M * N /= ROW_NUM.'
    write ( *, '(a)' ) '  That is, the number of lines of data in the file'
    write ( *, '(a,i8)' ) '  which is ROW_NUM = ', row_num
    write ( *, '(a)' ) '  does not correspond to the product of'
    write ( *, '(a,i8)' ) '  the number of grid rows M = ', m
    write ( *, '(a,i8)' ) '  and grid columns N = ', n
    stop
  end if

  write ( *, '(a)' ) ' '
  write ( *, '(a)' ) '  First input file: "' // trim ( input_file_name ) // '".'
  write ( *, '(a)' ) ' '
  write ( *, '(a)' ) '  Input data dimensions:'
  write ( *, '(a)' ) ' '
  write ( *, '(a,i8)' ) '  File rows ROW_NUM =       ', row_num
  write ( *, '(a,i8)' ) '  File columns COLUMN_NUM = ', column_num
  write ( *, '(a,i8)' ) '  Grid rows M =             ', m
  write ( *, '(a,i8)' ) '  Grid columns N =          ', n

  row_num2 = ( m + 2 ) * ( n + 2 )
  column_num2 = column_num
  m2 = m + 2
  n2 = n + 2
  
  write ( *, '(a)' ) ' '
  write ( *, '(a)' ) '  First output file: "' &
    // trim ( output_file_name ) // '".'
  write ( *, '(a)' ) ' '
  write ( *, '(a)' ) '  Output data dimensions:'
  write ( *, '(a)' ) ' '
  write ( *, '(a,i8)' ) '  File rows ROW_NUM2 =       ', row_num2
  write ( *, '(a,i8)' ) '  File columns COLUMN_NUM2 = ', column_num2
  write ( *, '(a,i8)' ) '  Grid rows M2 =             ', m2
  write ( *, '(a,i8)' ) '  Grid columns N2 =          ', n2
!
!  Allocate space.
!
  allocate ( u(row_num,column_num) )
  allocate ( v(row_num2,column_num2) )

  input_file_num = 0

  do

    if ( .not. file_exist ( input_file_name ) ) then
      exit
    end if

    input_file_num = input_file_num + 1
!
!  Read the U vectors.
!
    call dtable_data_read ( input_file_name, column_num, row_num, u ) 
!
!  Print a bit of the input array.
!
    if ( input_file_num == 1 ) then
      ihi = min ( m, 7 )
      jhi = min ( n, 7 )
      call r8mat_print_some ( m, n, u, 1, 1, ihi, jhi, &
        '  Upper left corner of first input matrix:' )
    end if
!
!  Add the border.
!
    call r8mat_border_add ( m, n, u, v )
!
!  Print a bit of the output array.
!
    if ( input_file_num == 1 ) then
      ihi = min ( m2, 7 )
      jhi = min ( n2, 7 )
      call r8mat_print_some ( m2, n2, v, 1, 1, ihi, jhi, &
        '  Upper left corner of first output matrix:' )
    end if
!
!  Write the V vectors.
!
    call r8mat_write ( output_file_name, column_num2, row_num2, v )
!
!  Increment the file name.
!
    call file_name_inc ( input_file_name )
    call file_name_inc ( output_file_name )

  end do

  write ( *, '(a)' ) ' '
  write ( *, '(a,i8)' ) '  Number of files processed was ', input_file_num
!
!  Free memory.
!
  deallocate ( u )
  deallocate ( v )
!
!  Terminate.
!
  write ( *, '(a)' ) ' '
  write ( *, '(a)' ) 'TABLE_BORDER:'
  write ( *, '(a)' ) '  Normal end of execution.'

  write ( *, '(a)' ) ' '
  call timestamp ( )

  stop
end
subroutine ch_cap ( c )

!*****************************************************************************80
!
!! CH_CAP capitalizes a single character.
!
!  Licensing:
!
!    This code is distributed under the GNU LGPL license. 
!
!  Modified:
!
!    19 July 1998
!
!  Author:
!
!    John Burkardt
!
!  Parameters:
!
!    Input/output, character C, the character to capitalize.
!
  implicit none

  character c
  integer ( kind = 4 ) itemp

  itemp = ichar ( c )

  if ( 97 <= itemp .and. itemp <= 122 ) then
    c = char ( itemp - 32 )
  end if

  return
end
function ch_eqi ( c1, c2 )

!*****************************************************************************80
!
!! CH_EQI is a case insensitive comparison of two characters for equality.
!
!  Example:
!
!    CH_EQI ( 'A', 'a' ) is TRUE.
!
!  Licensing:
!
!    This code is distributed under the GNU LGPL license. 
!
!  Modified:
!
!    28 July 2000
!
!  Author:
!
!    John Burkardt
!
!  Parameters:
!
!    Input, character C1, C2, the characters to compare.
!
!    Output, logical CH_EQI, the result of the comparison.
!
  implicit none

  character c1
  character c1_cap
  character c2
  character c2_cap
  logical ch_eqi

  c1_cap = c1
  c2_cap = c2

  call ch_cap ( c1_cap )
  call ch_cap ( c2_cap )

  if ( c1_cap == c2_cap ) then
    ch_eqi = .true.
  else
    ch_eqi = .false.
  end if

  return
end
function ch_is_digit ( c )

!*****************************************************************************80
!
!! CH_IS_DIGIT returns TRUE if a character is a decimal digit.
!
!  Licensing:
!
!    This code is distributed under the GNU LGPL license. 
!
!  Modified:
!
!    09 August 1999
!
!  Author:
!
!    John Burkardt
!
!  Parameters:
!
!    Input, character C, the character to be analyzed.
!
!    Output, logical CH_IS_DIGIT, TRUE if C is a digit, FALSE otherwise.
!
  implicit none

  character c
  logical ch_is_digit

  if ( lge ( c, '0' ) .and. lle ( c, '9' ) ) then
    ch_is_digit = .true.
  else
    ch_is_digit = .false.
  end if

  return
end
subroutine ch_to_digit ( c, digit )

!*****************************************************************************80
!
!! CH_TO_DIGIT returns the integer value of a base 10 digit.
!
!  Example:
!
!     C   DIGIT
!    ---  -----
!    '0'    0
!    '1'    1
!    ...  ...
!    '9'    9
!    ' '    0
!    'X'   -1
!
!  Licensing:
!
!    This code is distributed under the GNU LGPL license. 
!
!  Modified:
!
!    04 August 1999
!
!  Author:
!
!    John Burkardt
!
!  Parameters:
!
!    Input, character C, the decimal digit, '0' through '9' or blank
!    are legal.
!
!    Output, integer ( kind = 4 ) DIGIT, the corresponding integer value.  
!    If C was 'illegal', then DIGIT is -1.
!
  implicit none

  character c
  integer ( kind = 4 ) digit

  if ( lge ( c, '0' ) .and. lle ( c, '9' ) ) then

    digit = ichar ( c ) - 48

  else if ( c == ' ' ) then

    digit = 0

  else

    digit = -1

  end if

  return
end
subroutine dtable_data_read ( input_filename, m, n, table )

!*****************************************************************************80
!
!! DTABLE_DATA_READ reads data from a double precision table file.
!
!  Discussion:
!
!    The file may contain more than N points, but this routine will
!    return after reading N of them.
!
!  Licensing:
!
!    This code is distributed under the GNU LGPL license. 
!
!  Modified:
!
!    07 September 2004
!
!  Author:
!
!    John Burkardt
!
!  Parameters:
!
!    Input, character ( len = * ) INPUT_FILENAME, the name of the input file.
!
!    Input, integer ( kind = 4 ) M, the spatial dimension.
!
!    Input, integer ( kind = 4 ) N, the number of points.
!
!    Output, real ( kind = 8 ) TABLE(M,N), the table data.
!
  implicit none

  integer ( kind = 4 ) m
  integer ( kind = 4 ) n

  integer ( kind = 4 ) ierror
  character ( len = * ) input_filename
  integer ( kind = 4 ) input_unit
  integer ( kind = 4 ) ios
  integer ( kind = 4 ) j
  character ( len = 255 ) line
  real ( kind = 8 ) table(m,n)
  real ( kind = 8 ) x(m)

  ierror = 0

  call get_unit ( input_unit )

  open ( unit = input_unit, file = input_filename, status = 'old', &
    iostat = ios )

  if ( ios /= 0 ) then
    ierror = 1
    write ( *, '(a)' ) ' '
    write ( *, '(a)' ) 'DTABLE_DATA_READ - Fatal error!'
    write ( *, '(a)' ) '  Could not open the input file: ' // &
      trim ( input_filename )
    stop
  end if

  j = 0

  do while ( j < n )

    read ( input_unit, '(a)', iostat = ios ) line

    if ( ios /= 0 ) then
      ierror = 2
      write ( *, '(a)' ) ' '
      write ( *, '(a)' ) 'DTABLE_DATA_READ - Fatal error!'
      write ( *, '(a)' ) '  Error while reading lines of data.'
      write ( *, '(a,i8)' ) '  Number of values expected per line M = ', m
      write ( *, '(a,i8)' ) '  Number of data lines read, J =         ', j
      write ( *, '(a,i8)' ) '  Number of data lines needed, N =       ', n
      stop
    end if

    if ( line(1:1) == '#' .or. len_trim ( line ) == 0 ) then
      cycle
    end if

    call s_to_r8vec ( line, m, x, ierror )

    if ( ierror /= 0 ) then
      ierror = 0
      cycle
    end if

    j = j + 1

    table(1:m,j) = x(1:m)

  end do

  close ( unit = input_unit )

  return
end
subroutine dtable_header_read ( input_filename, column_num, row_num )

!*****************************************************************************80
!
!! DTABLE_HEADER_READ reads the header from a double precision table file.
!
!  Licensing:
!
!    This code is distributed under the GNU LGPL license. 
!
!  Modified:
!
!    07 September 2004
!
!  Author:
!
!    John Burkardt
!
!  Parameters:
!
!    Input, character ( len = * ) INPUT_FILENAME, the name of the input file.
!
!    Output, integer ( kind = 4 ) COLUMN_NUM, the number of columns of data.
!
!    Output, integer ( kind = 4 ) ROW_NUM, the number of rows of data. 
!
  implicit none

  integer ( kind = 4 ) column_num
  character ( len = * ) input_filename
  integer ( kind = 4 ) row_num

  call file_column_count ( input_filename, column_num )

  if ( column_num <= 0 ) then
    write ( *, '(a)' ) ' '
    write ( *, '(a)' ) 'DTABLE_HEADER_READ - Fatal error!'
    write ( *, '(a)' ) '  There was some kind of I/O problem while trying'
    write ( *, '(a)' ) '  to count the number of data columns in'
    write ( *, '(a)' ) '  the file "' // trim ( input_filename ) // '".'
    stop
  end if

  call file_row_count ( input_filename, row_num )

  if ( row_num <= 0 ) then
    write ( *, '(a)' ) ' '
    write ( *, '(a)' ) 'DTABLE_HEADER_READ - Fatal error!'
    write ( *, '(a)' ) '  There was some kind of I/O problem while trying'
    write ( *, '(a)' ) '  to count the number of data rows in'
    write ( *, '(a)' ) '  the file "' // trim ( input_filename ) // '".'
    stop
  end if

  return
end
subroutine file_column_count ( input_filename, column_num )

!*****************************************************************************80
!
!! FILE_COLUMN_COUNT counts the number of columns in the first line of a file.
!
!  Discussion:
!
!    The file is assumed to be a simple text file.
!
!    Most lines of the file is presumed to consist of COLUMN_NUM words,
!    separated by spaces.  There may also be some blank lines, and some 
!    comment lines,
!    which have a "#" in column 1.
!
!    The routine tries to find the first non-comment non-blank line and
!    counts the number of words in that line.
!
!    If all lines are blanks or comments, it goes back and tries to analyze
!    a comment line.
!
!  Licensing:
!
!    This code is distributed under the GNU LGPL license. 
!
!  Modified:
!
!    21 June 2001
!
!  Author:
!
!    John Burkardt
!
!  Parameters:
!
!    Input, character ( len = * ) INPUT_FILENAME, the name of the file.
!
!    Output, integer ( kind = 4 ) COLUMN_NUM, the number of columns in the file.
!
  implicit none

  integer ( kind = 4 ) column_num
  logical got_one
  character ( len = * ) input_filename
  integer ( kind = 4 ) input_unit
  integer ( kind = 4 ) ios
  character ( len = 256 ) line
!
!  Open the file.
!
  call get_unit ( input_unit )

  open ( unit = input_unit, file = input_filename, status = 'old', &
    form = 'formatted', access = 'sequential', iostat = ios )

  if ( ios /= 0 ) then
    column_num = -1
    write ( *, '(a)' ) ' '
    write ( *, '(a)' ) 'FILE_COLUMN_COUNT - Fatal error!'
    write ( *, '(a)' ) '  Could not open the file:'
    write ( *, '(a)' ) '    ' // trim ( input_filename )
    return
  end if
!
!  Read one line, but skip blank lines and comment lines.
!
  got_one = .false.

  do

    read ( input_unit, '(a)', iostat = ios ) line

    if ( ios /= 0 ) then
      exit
    end if

    if ( len_trim ( line ) == 0 ) then
      cycle
    end if

    if ( line(1:1) == '#' ) then
      cycle
    end if

    got_one = .true.
    exit

  end do

  if ( .not. got_one ) then

    rewind ( input_unit )

    do

      read ( input_unit, '(a)', iostat = ios ) line

      if ( ios /= 0 ) then
        exit
      end if

      if ( len_trim ( line ) == 0 ) then
        cycle
      end if

      got_one = .true.
      exit

    end do

  end if

  close ( unit = input_unit )

  if ( .not. got_one ) then
    write ( *, '(a)' ) ' '
    write ( *, '(a)' ) 'FILE_COLUMN_COUNT - Warning!'
    write ( *, '(a)' ) '  The file does not seem to contain any data.'
    column_num = -1
    return
  end if

  call s_word_count ( line, column_num )

  return
end
function file_exist ( file_name )

!*****************************************************************************80
!
!! FILE_EXIST reports whether a file exists.
!
!  Licensing:
!
!    This code is distributed under the GNU LGPL license. 
!
!  Modified:
!
!    19 February 2002
!
!  Author:
!
!    John Burkardt
!
!  Parameters:
!
!    Input, character ( len = * ) FILE_NAME, the name of the file.
!
!    Output, logical FILE_EXIST, is TRUE if the file exists.
!
  implicit none

  character ( len = * ) file_name
  logical file_exist

  inquire ( file = file_name, exist = file_exist )

  return
end
subroutine file_name_inc ( file_name )

!*****************************************************************************80
!
!! FILE_NAME_INC generates the next filename in a series.
!
!  Discussion:
!
!    It is assumed that the digits in the name, whether scattered or
!    connected, represent a number that is to be increased by 1 on
!    each call.  If this number is all 9's on input, the output number
!    is all 0's.  Non-numeric letters of the name are unaffected, and
!    if the name contains no digits, then nothing is done.
!
!  Example:
!
!      Input          Output
!      -----          ------
!      a7to11.txt     a7to12.txt
!      a7to99.txt     a8to00.txt
!      a9to99.txt     a0to00.txt
!      cat.txt        cat.txt
!
!  Licensing:
!
!    This code is distributed under the GNU LGPL license. 
!
!  Modified:
!
!    12 October 2003
!
!  Author:
!
!    John Burkardt
!
!  Parameters:
!
!    Input/output, character ( len = * ) FILE_NAME.
!    On input, a character string to be incremented.
!    On output, the incremented string.
!
  implicit none

  character c
  integer ( kind = 4 ) digit
  character ( len = * ) file_name
  integer ( kind = 4 ) i
  integer ( kind = 4 ) lens

  lens = len_trim ( file_name )

  do i = lens, 1, -1

    c = file_name(i:i)

    if ( lge ( c, '0' ) .and. lle ( c, '9' ) ) then

      digit = ichar ( c ) - 48
      digit = digit + 1

      if ( digit == 10 ) then
        digit = 0
      end if

      c = char ( digit + 48 )

      file_name(i:i) = c

      if ( c /= '0' ) then
        return
      end if

    end if

  end do

  return
end
subroutine file_row_count ( input_filename, row_num )

!*****************************************************************************80
!
!! FILE_ROW_COUNT counts the number of row records in a file.
!
!  Discussion:
!
!    It does not count lines that are blank, or that begin with a
!    comment symbol '#'.
!
!  Licensing:
!
!    This code is distributed under the GNU LGPL license. 
!
!  Modified:
!
!    06 March 2003
!
!  Author:
!
!    John Burkardt
!
!  Parameters:
!
!    Input, character ( len = * ) INPUT_FILENAME, the name of the input file.
!
!    Output, integer ( kind = 4 ) ROW_NUM, the number of rows found.
!
  implicit none

  integer ( kind = 4 ) bad_num
  integer ( kind = 4 ) comment_num
  integer ( kind = 4 ) ierror
  character ( len = * ) input_filename
  integer ( kind = 4 ) input_unit
  integer ( kind = 4 ) ios
  character ( len = 100 ) line
  integer ( kind = 4 ) record_num
  integer ( kind = 4 ) row_num

  call get_unit ( input_unit )

  open ( unit = input_unit, file = input_filename, status = 'old', &
    iostat = ios )

  if ( ios /= 0 ) then
    row_num = -1;
    ierror = 1
    write ( *, '(a)' ) ' '
    write ( *, '(a)' ) 'FILE_ROW_COUNT - Fatal error!'
    write ( *, '(a)' ) '  Could not open the input file: ' // &
      trim ( input_filename )
    stop
  end if

  comment_num = 0
  row_num = 0
  record_num = 0
  bad_num = 0

  do

    read ( input_unit, '(a)', iostat = ios ) line

    if ( ios /= 0 ) then
      ierror = record_num
      exit
    end if

    record_num = record_num + 1

    if ( line(1:1) == '#' ) then
      comment_num = comment_num + 1
      cycle
    end if

    if ( len_trim ( line ) == 0 ) then
      comment_num = comment_num + 1
      cycle
    end if

    row_num = row_num + 1

  end do

  close ( unit = input_unit )

  return
end
subroutine get_unit ( iunit )

!*****************************************************************************80
!
!! GET_UNIT returns a free FORTRAN unit number.
!
!  Discussion:
!
!    A "free" FORTRAN unit number is an integer between 1 and 99 which
!    is not currently associated with an I/O device.  A free FORTRAN unit
!    number is needed in order to open a file with the OPEN command.
!
!    If IUNIT = 0, then no free FORTRAN unit could be found, although
!    all 99 units were checked (except for units 5, 6 and 9, which
!    are commonly reserved for console I/O).
!
!    Otherwise, IUNIT is an integer between 1 and 99, representing a
!    free FORTRAN unit.  Note that GET_UNIT assumes that units 5 and 6
!    are special, and will never return those values.
!
!  Licensing:
!
!    This code is distributed under the GNU LGPL license. 
!
!  Modified:
!
!    18 September 2005
!
!  Author:
!
!    John Burkardt
!
!  Parameters:
!
!    Output, integer ( kind = 4 ) IUNIT, the free unit number.
!
  implicit none

  integer ( kind = 4 ) i
  integer ( kind = 4 ) ios
  integer ( kind = 4 ) iunit
  logical lopen

  iunit = 0

  do i = 1, 99

    if ( i /= 5 .and. i /= 6 .and. i /= 9 ) then

      inquire ( unit = i, opened = lopen, iostat = ios )

      if ( ios == 0 ) then
        if ( .not. lopen ) then
          iunit = i
          return
        end if
      end if

    end if

  end do

  return
end
subroutine i4_input ( string, value, ierror )

!*****************************************************************************80
!
!! I4_INPUT prints a prompt string and reads an integer from the user.
